# -------Binary And--------
a = 50           # 110010
b = 25           # 011001
print(a & b)     # 010000
#--------Binary Shift------
x = 240          #11110000
y = x >> 2       #00111100
print(y)
x = 240
y = x << 2
print(y)        #1111000000
#-------Binary Or ------------
a = 50           # 110010
b = 25           # 011001
print(a | b)     # 111011
#-------Binary not or ----------
a = 50           # 110010
b = 25           # 011001
print(a ^ b)     # 101011
